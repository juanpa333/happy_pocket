<h3 class="text-center mb-4">POLITICA DE COOKIES</h3>

<h4 class="text-center mb-4">Información sobre las cookies</h5>

<h5 class="text-center">¿Qué son las cookies?</h4>
<p class="">
  Las cookies y otras tecnologías similares tales como local shared objects, flash cookies o píxeles, son herramientas
empleadas por los servidores Web para almacenar y recuperar información acerca de sus visitantes, así como para ofrecer
un correcto funcionamiento del sitio.
</p>
<p class="">
Mediante el uso de estos dispositivos se permite al servidor Web recordar algunos datos concernientes al usuario, como
sus preferencias para la visualización de las páginas de ese servidor, nombre y contraseña, productos que más le
interesan, etc.
</p>
<h5 class="text-center">COOKIES AFECTADAS POR LA NORMATIVA Y COOKIES EXCEPTUADAS</h5>
<p class="">
  Según la directiva de la UE, la FCC y el ministerio de las TIC en Colombia, las cookies que requieren el consentimiento informado por parte del usuario son las cookies de
analítica y las de publicidad y afiliación, quedando exceptuadas las de carácter técnico y las necesarias para el
funcionamiento del sitio web o la prestación de servicios expresamente solicitados por el usuario.
</p>

<h5 class="text-center">¿QUÉ TIPOS DE COOKIES EXISTEN?</h5>
<p class="">
  Sobre los tipos de cookies, existen cinco grandes grupos:
<ul>
<li>  Cookies analíticas: recogen información del uso que se realiza del sitio web.</li>
<li>Cookies sociales: son aquellas necesarias para redes sociales externas.</li>
<li>Cookies de afiliados: permiten hacer un seguimiento de las visitas procedentes de otras webs, con las que el
sitio web establece un contrato de afiliación (empresas de afiliación).</li>
<li>Cookies de publicidad y comportamentales: recogen información sobre las preferencias y elecciones
personales del usuario (retargeting).</li>
<li>Cookies técnicas y funcionales: son las estrictamente necesarias para el uso del sitio web y para la prestación
del servicio contratado.</li>
</ul>
</p>

<h5 class="text-center">COOKIES QUE SE UTILIZAN EN ESTE SITIO WEB</h5>

<ul>
  <li>PHPSESSID: cookie técnica y estrictamente necesaria que contiene el identificador de la sesión. Se elimina al
    cerrar el navegador.</li>
  <li>_lang: cookie técnica y estrictamente necesaria que contiene el idioma de la sesión. Se elimina al cerrar el
    navegador.</li>
  <li>ac_cookies: cookie técnica y estrictamente necesaria que contiene el valor de si se ha aceptado la instalación
    de cookies. Caduca en 1 año desde la última actualización.
    </li>
  <li>_ga: cookie de Google Analytics que habilita la función de control de visitas únicas. La primera vez que un
    usuario entre en el sitio web a través de un navegador se instalará esta cookie. Cuando este usuario vuelva a
    entrar en la web con el mismo navegador, la cookie considerará que es el mismo usuario. Solo en el caso de que el
    usuario cambie de navegador, se considerará otro usuario. Caduca a los 2 años desde la última actualización</li>
  <li>_gat: Esta cookie se asocia con Google Analytics Universal. Se utiliza para limitar la velocidad de petición - la
    limitación de la recogida de datos en los sitios de alto tráfico. Caduca a los 10 minutos.
    </li>
  <li>_utma: cookie de Google Analytics que registra la fecha de la primera y última vez que el usuario vistió el sitio
    web. Caduca a los 2 años desde la última actualización.</li>
  <li>_utmb: cookie de Google Analytics que registra la hora de llegada a la página web. Caduca a los 30 minutos
    desde la última actualización.</li>
  <li>_utmc: cookie de Google Analytics utilizada para la interoperabilidad con el código de seguimiento urchin.js. Se
    elimina al cerrar el navegador</li>
  <li>_utmt: cookie de Google Analytics. Esta cookie se utiliza para procesar el tipo de solicitud pedida por el
    usuario. Caduca al finalizar la sesión</li>
  <li>_utmv: cookie de Google Analytics. Esta cookie se utiliza para segmentar datos demográficos. Caduca al
    finalizar la sesión.
    </li>
    <li>_utmz: cookie de Google Analytics que almacena la fuente de tráfico o una campaña para explicar cómo el
      usuario llegó al sitio web. Caduca a los 6 meses desde la última actualización.
      </li>

</ul>

<h5 class="text-center"> REVOCACIÓN DEL CONSENTIMIENTO PARA INSTALAR COOKIES</h5>
<h5 class="text-center">COMO ELIMINAR LAS COOKIES DEL NAVEGADOR</h5>

<h5>Chrome</h5>
<ol>
  <li>Selecciona el icono de Herramientas </li>
  <li>Haz clic en Configuración.</li>
  <li>Haz clic en Mostrar Opciones Avanzadas.
  </li>
  <li>En la sección "Privacidad" haz clic en Configuración de contenido.
    <ul>
      <li>Eliminar cookies: Haz clic en Todas las cookies y los datos de sitios…</li>
      <li> No permitir que se almacenen cookies</li>
    </ul>
  </li>
  <li>Haz clic en Eliminar datos de navegación (vaciar la Caché)</li>
  <li>Cierra y reinicia el navegador</li>
</ol>

<p class="">
  Para más información sobre Chrome pulse aquí:<a target="_blank" href="http://support.google.com/chrome/answer/95647?hl=es"> http://support.google.com/chrome/answer/95647?hl=es</a>
</p>

<h5 class="">Internet Explorer. Versión 11</h5>

<ol>
  <li>Selecciona Herramientas | Opciones de Internet</li>
  <li>Haz clic en la ficha General</li>
  <li>En la sección "Historial de exploración", haz clic en Eliminar el historial de exploración al salir</li>
  <li>Seleccionar Eliminar archivos.</li>
  <li>Seleccionar Eliminar cookies</li>
  <li>Haz clic en Eliminar</li>
  <li>Haz clic en Aceptar</li>
  <li>Cierra y reinicia el navegador</li>
</ol>

<h5 class="">Firefox. Versión 18</h5>

<ol>
  <li>Selecciona Firefox | Historial | Limpiar el historial reciente</li>
  <li> Al lado de "Detalles", haz clic en la flecha hacia abajo</li>
  <li> Selecciona las siguientes casillas de verificación: Cookies, Caché, Inicios de sesión activos</li>
  <li>Usando el "Intervalo de tiempo para borrar" en el menú desplegable, selecciona Todo</li>
  <li>Haz clic en Borrar ahora</li>
  <li>Cierra y reinicia el navegador</li>
 
</ol>

<p>Puede aceptar o rechazar las cookies individualmente en las Preferencias de Firefox, en la sección Historial disponible en
  Herramientas > Opciones > Privacidad.</p>
<p>Para más información sobre Mozilla Firefox pulse aquí: <a href="https://www.mozilla.org/es-ES/privacy/websites/#cookies" target="_blank"> https://www.mozilla.org/es-ES/privacy/websites/#cookies</a></p>

<h5 class="">Safari Versión 5.1</h5>

<ol>
  <li>Selecciona el icono de Safari / Editar | Restaurar Safari</li>
  <li>Selecciona las siguientes casillas de verificación: Borrar el historial, Eliminar todos los datos de sitio web</li>
  <li>Haz clic en Restablecer</li>
  <li>Cierra y reinicia el navegador</li>
  
 
</ol>


<p>Para más información sobre Safari pulse aquí: <a href="http://support.apple.com/kb/PH5042" target="_blank">http://support.apple.com/kb/PH5042</a></p>

